// What is a Variable?
// In programming, a variable is like a container or a box that can hold different types of data. 
//It has a name (identifier) and a value. You can think of it as a way to store and manipulate data in your programs.

// How to Declare a Variable:
// In Dart, you declare a variable using the var, final, or const keyword followed by the variable name and an optional type annotation 
//(the data type of the variable).

// Using var:
// The var keyword is used when the data type can be inferred by the Dart compiler. Here's an example:

var age = 25; // Dart infers that age is an integer.
var name = "John"; // Dart infers that name is a string.
